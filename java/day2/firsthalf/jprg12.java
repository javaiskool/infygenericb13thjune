//multiple if demo 
class Tester
{
	public static void main(String s[])
	{
		
		int x=12,y=6,z=8;
		
		if (x>=y && x>=z)
		{
			System.out.println("x is maximum");
		}
		if (y>=x && y>=z)
		{
			System.out.println("y is maximum");
		}
		if (z>=x && z>=y)
		{
			System.out.println("z is maximum");
		}
	}

}