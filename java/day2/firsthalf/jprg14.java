//multiple if demo 
class Tester
{
	public static void main(String s[])
	{
		
		int x=12,y=6,ch=3,r;
		
		if (ch==1)
		{
			r=x+y;
			System.out.println(x+" + "+y+" = "+r);
		}
		if (ch==2)
		{
			r=x-y;
			System.out.println(x+" - "+y+" = "+r);
		}
		if (ch==3)
		{
			r=x*y;
			System.out.println(x+" * "+y+" = "+r);
		}
		if (ch==4)
		{
			r=x/y;
			System.out.println(x+" / "+y+" = "+r);
		}

	}
}