// unary operators operator demo
class Tester
{
	public static void main(String s[])
	{
		int x=2,y;
		y=++x;
		System.out.println("x = " +x);
		System.out.println("y = " +y);
		int a=2,b;
		b=a++;
		System.out.println("a = " +a);
		System.out.println("b = " +b);
		boolean bl=true;
		System.out.println("bl =  " +bl);
		System.out.println("!bl = " +!bl);

	}
}